using BenchmarkDotNet.Attributes;
using LabEG.NeedleCrud.Models.ViewModels.PaginationViewModels;
using LabEG.NeedleCrud.Repositories;
using LabEG.NeedleCrud.Settings;
using LabEG.NeedleCrud.TestsFixtures.BLL.Entities;
using LabEG.NeedleCrud.TestsFixtures.DAL;
using LabEG.NeedleCrud.TestsFixtures.Fixtures;
using Microsoft.EntityFrameworkCore;

namespace LabEG.NeedleCrud.Benchmarks.Benchmarks;

/// <summary>
/// Benchmarks comparing performance of CrudDbRepository.GetPaged
/// against an equivalent direct DbContext implementation.
///
/// Direct DbContext benchmarks mirror the repository behavior exactly:
///   - no AsNoTracking (repository does not use it)
///   - same Skip(0).Take(pageSize).ToListAsync() pattern
///   - same TotalPages integer-arithmetic formula
///   - same LongCount() for ElementsInPage
///   - same include paths generated by ExtractIncludes:
///       "Author.Books.Category", "Category.Books", "Reviews.User"
/// </summary>
[MemoryDiagnoser]
[SimpleJob(warmupCount: 3, iterationCount: 15)]
public class GetPagedPerformanceComparisonBenchmarks
{
    private LibraryDbContext _context = null!;
    private CrudDbRepository<LibraryDbContext, Book, Guid> _repository = null!;
    private readonly NeedleCrudSettings _settings = new();
    private PagedListQuery _simpleQuery = null!;
    private PagedListQuery _mediumQuery = null!;
    private PagedListQuery _complexQuery = null!;

    // Leaf nodes must be null — ExtractIncludes adds a path only on JsonValueKind.Null.
    // This graph generates exactly three include paths:
    //   "Author.Books.Category", "Category.Books", "Reviews.User"
    private readonly string _graphJson = """
        {
            "author": {
                "books": {
                    "category": null
                }
            },
            "category": {
                "books": null
            },
            "reviews": {
                "user": null
            }
        }
        """;

    [Params(DatabaseProvider.InMemory, DatabaseProvider.PostgreSQL)]
    public DatabaseProvider Provider { get; set; }

    [GlobalSetup]
    public void GlobalSetup()
    {
        string databaseName = $"GetPagedComparisonBenchmarkDb_{Provider}_{Guid.NewGuid():N}";

        _context = LibraryDbContextFactory.Create(Provider, databaseName);
        _context.Database.EnsureDeleted();
        _context.Database.EnsureCreated();

        TestDataGenerator.SeedDatabase(_context, seed: 42);
        _context.ChangeTracker.Clear();

        _repository = new(_context);

        // Simple case: 1 filter, 1 sort, no graph
        _simpleQuery = new(
            pageSize: 10,
            pageNumber: 1,
            filter: "title~like~Book",
            sort: "title~asc",
            graph: null,
            settings: _settings
        );

        // Medium case: 3 filters, 3 sorts, graph 3 levels deep
        _mediumQuery = new(
            pageSize: 10,
            pageNumber: 1,
            filter: "title~like~Book,pageCount~>=~100,language~=~en",
            sort: "title~asc,pageCount~desc,publicationDate~desc",
            graph: _graphJson,
            settings: _settings
        );

        // Complex case: 4 filters, 3 sorts, graph 3 levels deep
        _complexQuery = new(
            pageSize: 10,
            pageNumber: 1,
            filter: "title~like~Book,pageCount~>=~100,language~=~en,isAvailable~=~true",
            sort: "title~asc,pageCount~desc,publicationDate~desc",
            graph: _graphJson,
            settings: _settings
        );
    }

    [GlobalCleanup]
    public void GlobalCleanup()
    {
        _context.Database.EnsureDeleted();
        _context.Dispose();
    }

    /// <summary>
    /// Clears the change tracker between iterations.
    /// Without this, tracked entities accumulate across iterations and slow down
    /// later iterations — making results incomparable between repository and direct methods.
    /// </summary>
    [IterationCleanup]
    public void IterationCleanup()
    {
        _context.ChangeTracker.Clear();
    }

    /// <summary>
    /// Builds a PagedList using the same formula as CrudDbRepository.GetPaged.
    /// </summary>
    private static PagedList<Book> BuildPagedList(List<Book> items, int total, int pageSize)
    {
        int extraCount = total % pageSize > 0 ? 1 : 0;
        return new PagedList<Book>
        {
            Items = items,
            Meta = new PageMeta
            {
                PageNumber = 1,
                PageSize = pageSize,
                TotalItems = total,
                TotalPages = total < pageSize ? 1 : (total / pageSize) + extraCount,
                ItemsInPage = items.LongCount()
            }
        };
    }

    // ─── Simple ──────────────────────────────────────────────────────────────

    [Benchmark]
    public async Task<PagedList<Book>> SimpleCase_RepositoryMethod()
    {
        return await _repository.GetPaged(_simpleQuery);
    }

    [Benchmark]
    public async Task<PagedList<Book>> SimpleCase_DirectDbContext()
    {
        // No includes (graph: null)
        // 1 where, 1 order-by
        IOrderedQueryable<Book> queryable = _context.Books
            .Where(b => b.Title.Contains("Book"))
            .OrderBy(b => b.Title);

        int total = await queryable.CountAsync();
        List<Book> items = await queryable
            .Skip(0)
            .Take(10)
            .ToListAsync();

        return BuildPagedList(items, total, 10);
    }

    // ─── Medium ──────────────────────────────────────────────────────────────

    [Benchmark]
    public async Task<PagedList<Book>> MediumCase_RepositoryMethod()
    {
        return await _repository.GetPaged(_mediumQuery);
    }

    [Benchmark]
    public async Task<PagedList<Book>> MediumCase_DirectDbContext()
    {
        // Includes: "Author.Books.Category", "Category.Books", "Reviews.User"
        // 3 filters, 3 sorts
        IOrderedQueryable<Book> queryable = _context.Books
            .Include(b => b.Author)
                .ThenInclude(a => a.Books)
                    .ThenInclude(b => b.Category)
            .Include(b => b.Category)
                .ThenInclude(c => c.Books)
            .Include(b => b.Reviews)
                .ThenInclude(r => r.User)
            .Where(b => b.Title.Contains("Book") && b.PageCount >= 100 && b.Language == "en")
            .OrderBy(b => b.Title)
            .ThenByDescending(b => b.PageCount)
            .ThenByDescending(b => b.PublicationDate);

        int total = await queryable.CountAsync();
        List<Book> items = await queryable
            .Skip(0)
            .Take(10)
            .ToListAsync();

        return BuildPagedList(items, total, 10);
    }

    // ─── Complex ─────────────────────────────────────────────────────────────

    [Benchmark]
    public async Task<PagedList<Book>> ComplexCase_RepositoryMethod()
    {
        return await _repository.GetPaged(_complexQuery);
    }

    [Benchmark]
    public async Task<PagedList<Book>> ComplexCase_DirectDbContext()
    {
        // Includes: "Author.Books.Category", "Category.Books", "Reviews.User"
        // 4 filters, 3 sorts
        IOrderedQueryable<Book> queryable = _context.Books
            .Include(b => b.Author)
                .ThenInclude(a => a.Books)
                    .ThenInclude(b => b.Category)
            .Include(b => b.Category)
                .ThenInclude(c => c.Books)
            .Include(b => b.Reviews)
                .ThenInclude(r => r.User)
            .Where(b => b.Title.Contains("Book") && b.PageCount >= 100 && b.Language == "en" && b.IsAvailable)
            .OrderBy(b => b.Title)
            .ThenByDescending(b => b.PageCount)
            .ThenByDescending(b => b.PublicationDate);

        int total = await queryable.CountAsync();
        List<Book> items = await queryable
            .Skip(0)
            .Take(10)
            .ToListAsync();

        return BuildPagedList(items, total, 10);
    }
}